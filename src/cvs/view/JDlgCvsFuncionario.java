    public CvsFuncionarios viewBean() {
        CvsFuncionarios func = new CvsFuncionarios();

        func.setCvsIdFuncionario(Util.strToInt(jTxtIdFuncionario.getText()));
        func.setCvsNome(jTxtCvsNome.getText());
        func.setCvsFuncao(jTxtFuncao.getText());
        func.setCvsCpf(jFmtCvsCpf.getText());
        func.setCvsEmail(jTxtCvsEmail.getText());
        func.setCvsTelefone(jFmtCvsTelefone.getText());
        func.setCvsDataNascimento(Util.strToDate(jFmtCvsDataNasc.getText()));

        return func;
    }

    public void beanView(CvsFuncionarios func) {
        jTxtIdFuncionario.setText(Util.intToStr(func.getCvsIdFuncionario()));
        jTxtCvsNome.setText(func.getCvsNome());
        jTxtFuncao.setText(func.getCvsFuncao());
        jFmtCvsCpf.setText(func.getCvsCpf());
        jTxtCvsEmail.setText(func.getCvsEmail());
        jFmtCvsTelefone.setText(func.getCvsTelefone());
        jFmtCvsDataNasc.setText(Util.dateToStr(func.getCvsDataNascimento()));
    }

