    private void jBtnCvsExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnCvsExcluirActionPerformed
        // TODO add your handling code here:
        if (pesquisando == false) {
            Util.mensagem("Você precisa pesquisar um usuário primeiro");
        } else {
            Util.perguntar("Você deseja excluir?");
            Util.limpar(jTxtIdFuncionario, jTxtCvsNome, jFmtCvsDataNasc, jTxtCvsEmail, jFmtCvsCpf, jFmtCvsTelefone, jTxtFuncao);
        }
    }//GEN-LAST:event_jBtnCvsExcluirActionPerformed

