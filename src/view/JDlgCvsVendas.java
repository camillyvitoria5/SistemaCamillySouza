    private void jBtnCvsExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnCvsExcluirActionPerformed
        //TODO add your handling code here:
        if (pesquisando == false) {
            Util.mensagem("VocÃª precisa pesquisar primeiro");
        } else {
            if (Util.perguntar("Deseja Excluir?") == true) {
                CvsVendasDAO vendasDAO = new CvsVendasDAO();
                vendasDAO.delete(viewBean());
                limparCampos();
            }
        }
    }//GEN-LAST:event_jBtnCvsExcluirActionPerformed

    private void jBtnCVsConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnCVsConfirmarActionPerformed
        // TODO add your handling code here:
        CvsVendasDAO vendasDAO = new CvsVendasDAO();
        CvsVendasJoiasDAO cvsVendasJoiasDAO = new CvsVendasJoiasDAO();

        if (incluir == true) {
            vendasDAO.insert(viewBean());
            for (int ind = 0; ind < jTblVendaJoias.getRowCount(); ind++) {
                CvsVendasjoias cvsVendasJoias = (CvsVendasjoias) controllerCvsVendasJoias.getBean(ind);
                cvsVendasJoias.setCvsVendas(viewBean());
                cvsVendasJoiasDAO.insert(cvsVendasJoias);
            }
        } else {
            vendasDAO.update(viewBean());
        }
        habilitar(false);
        limparCampos();
    }//GEN-LAST:event_jBtnCVsConfirmarActionPerformed

